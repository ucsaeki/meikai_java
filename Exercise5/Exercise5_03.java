public class Exercise5_03 {
	/* 
	 * 関数名  main
	 * 概要 論理型の変数にtrueとfalseを代入してその値を表示する
	 * 引数 なし
	 * 返り値 なし
	 * 作成者 Y.Saeki
	 * 作成日 2024/04/15
	 */
	public static void main(String[] args) {
		
		//論理型の変数にtrueを代入
		boolean trueVariable = true;
		//論理型の変数にfalseを代入
		boolean falseVariable = false;
		
		//変数の値を表示。
		System.out.println(trueVariable);
		//変数の値を表示。
		System.out.println(falseVariable);
		
	}

}
